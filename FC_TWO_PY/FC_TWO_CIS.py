# initialize CIS


def CIS(CIS_imput, out_lowest, out_highest, CIS_lowest=982, CIS_highest=2006):

    # calculate CIS
    CIS_difference = CIS_highest - CIS_lowest

    CIS_out = (((out_highest - out_lowest) / CIS_difference) * (CIS_difference - (CIS_highest - CIS_imput))) + out_lowest

    # returns CIS
    return CIS_out


